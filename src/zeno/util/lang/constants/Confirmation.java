package zeno.util.lang.constants;

/**
 * The {@code Confirmation} enumeration defines all states of confirmation.
 *
 * @since Aug 16, 2015
 * @author Zeno
 */
public enum Confirmation
{
	/**
	 * The confirmation has been cancelled.
	 */
	CANCELLED,
	/**
	 * The confirmation has been confirmed.
	 */
	CONFIRMED,
	/**
	 * The confirmation is still pending.
	 */
	PENDING;
}