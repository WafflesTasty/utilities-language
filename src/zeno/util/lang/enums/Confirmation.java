package zeno.util.lang.enums;

/**
 * The {@code Confirmation} enumeration defines confirmation states.
 *
 * @author Waffles
 * @since Aug 16, 2015
 * @version 1.0
 */
public enum Confirmation
{
	/**
	 * The confirmation has been cancelled.
	 */
	CANCELLED,
	/**
	 * The confirmation has been confirmed.
	 */
	CONFIRMED,
	/**
	 * The confirmation is still pending.
	 */
	PENDING;
}