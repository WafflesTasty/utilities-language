/**
 * An {@code Existence} defines states of existing.
 *
 * @author Waffles
 * @since 18 Sep 2025
 * @version 1.1
 * 
 * 
 * @see Existence
 */
public enum Existence
{
	/**
	 * Obligatory existence.
	 */
	OBLIGATORY,
	/**
	 * Optional existence.
	 */
	OPTIONAL,
	/**
	 * Non-existence.
	 */
	ABSENT;
}