package waffles.utils.lang.utilities.enums;

/**
 * A {@code Confirmation} defines a state of confirmation.
 *
 * @author Waffles
 * @since Aug 16, 2015
 * @version 1.0
 */
public enum Confirmation
{
	/**
	 * The confirmation has been cancelled.
	 */
	CANCELLED,
	/**
	 * The confirmation has been confirmed.
	 */
	CONFIRMED,
	/**
	 * The confirmation is still pending.
	 */
	PENDING;
}